package pessoa;

public class Pessoa {
    
	private String Nome;
	private String RG;
	private String CPF;
	private double Salario;
	
	public void setNome(String nome) {
		this.Nome = nome;
	}
	public void setRG(String rg) {
		this.RG = rg;
	}
	public void setCPF(String cpf) {
		this.CPF = cpf;
	}
	public void setSalario(double salario) {
		this.Salario = salario;
	}

}
