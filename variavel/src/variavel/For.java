package variavel;

public class For {

	public static void main(String[] args) {
		/*
		 Faça um programa para que o usuário visualize 10 números sequenciais de trás para
		 frente. O número inicial deverá se o 10.
		 
		 */
		
		
		/*
		 * int i;
		 * 
		 * for(i=0; i <= 10; i++) { System.out.println("A variável i agora vale " + i); }
		 */
		
		int i;
		
		for(i=10; i > 0; i--)
		{
			System.out.println("A variável i agora vale " + i);
		}
	}

}
